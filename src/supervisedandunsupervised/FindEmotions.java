    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
            /* Helpfulness Protocol */
        
        ArrayList Trust=new ArrayList();
        ArrayList Anticipation=new ArrayList();
        ArrayList Joy=new ArrayList();
        ArrayList Surprise=new ArrayList();
        ArrayList Anxiety=new ArrayList();
        ArrayList Anger=new ArrayList();
        ArrayList Sadness=new ArrayList();
        ArrayList Disgust=new ArrayList();        
        
        try
        {
            File fe2=new File("NRC-Emotion-Lexicon-Wordlevel-v0.92.txt");
            FileInputStream fis2=new FileInputStream(fe2);
            byte data2[]=new byte[fis2.available()];
            fis2.read(data2);
            fis2.close();
                
            String sg2[]=new String(data2).trim().split("\n");
               
            for(int i=0;i<sg2.length;i++)
            {
                if(sg2[i].trim().contains("\t"))
                {
                    String sp[]=sg2[i].trim().split("\t");
                    if(sp[1].trim().equals("trust"))
                    {
                        Trust.add(sp[0].trim());
                    }
                    if(sp[1].trim().equals("anticipation"))
                    {
                        Anticipation.add(sp[0].trim());
                    }
                    if(sp[1].trim().equals("joy"))
                    {
                        Joy.add(sp[0].trim());
                    }
                    if(sp[1].trim().equals("surprise"))
                    {
                        Surprise.add(sp[0].trim());
                    }
                    if(sp[1].trim().equals("anxiety"))
                    {
                        Anxiety.add(sp[0].trim());
                    }
                    if(sp[1].trim().equals("anger"))
                    {
                        Anger.add(sp[0].trim());
                    }
                    if(sp[1].trim().equals("sadness"))
                    {
                        Sadness.add(sp[0].trim());
                    }
                    if(sp[1].trim().equals("disgust"))
                    {
                        Disgust.add(sp[0].trim());
                    }
                }
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
        
        
        ArrayList allReviews=MainFrame.allReviews;
        
        jTextArea1.setText("");
        for(int k=0;k<allReviews.size();k++)
        {
            String rev=allReviews.get(k).toString().trim().replaceAll("[^\\w\\s]", "");     // replace all symbols
            String g2[]=rev.trim().split(" ");
            int trust=0,anticipation=0,joy=0,surprise=0,anxiety=0,anger=0,sadness=0,disgust=0;
            for(int j=0;j<g2.length;j++)
            {
                String g3=g2[j].trim();
                if(Trust.contains(g3.trim()))
                {
                    trust++;
                }
                if(Anticipation.contains(g3.trim()))
                {
                    anticipation++;
                }
                if(Joy.contains(g3.trim()))
                {
                    joy++;
                }
                if(Surprise.contains(g3.trim()))
                {
                    surprise++;
                }
                if(Anxiety.contains(g3.trim()))
                {
                    anxiety++;
                }
                if(Anger.contains(g3.trim()))
                {
                    anger++;
                }
                if(Sadness.contains(g3.trim()))
                {
                    sadness++;
                }
                if(Disgust.contains(g3.trim()))
                {
                    disgust++;
                }
            }
            
            int arr[]={trust,anticipation,joy,surprise,anxiety,anger,sadness,disgust};
            int maxValue=getMaxValue(arr);
            
            String res="Trust";
            if(maxValue == anticipation)
            {
                res="Anticipation";                
            }
            if(maxValue == joy)
            {
                res="Joy";                
            }
            if(maxValue == surprise)
            {
                res="Surprise";                
            }
            if(maxValue == anxiety)
            {
                res="Anxiety";                
            }
            if(maxValue == anger)
            {
                res="Anger";                
            }
            if(maxValue == sadness)
            {
                res="Sadness";                
            }
            if(maxValue == disgust)
            {
                res="Disgust";                
            }
            jTextArea1.append(rev.trim()+" --> "+res.trim()+"\n\n");
            allOpinions.add(res.trim());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

